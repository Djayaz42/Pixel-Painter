class LevelDefinition {
  const LevelDefinition({
    required this.name,
    required this.rows,
    required this.colorRuns,
    this.paintOrder = LevelPaintOrder.centerOut,
    this.cellRows = const [],
    this.gridRows = LevelData.rows,
    this.gridCols = LevelData.cols,
  });

  final String name;
  final List<String> rows;
  final List<LevelColorRun> colorRuns;
  final LevelPaintOrder paintOrder;
  final List<String> cellRows;
  final int gridRows;
  final int gridCols;
}

class LevelColorRun {
  const LevelColorRun(this.colorId, this.amount);

  final int colorId;
  final int amount;
}

