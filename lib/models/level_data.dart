  static List<PixelCell> createCells({
    int? levelIndex,
    LevelDefinition? level,
  }) {
    final lvl = level ?? levelAt(levelIndex ?? 0);
    List<PixelCell> cells = [];
    final rows = lvl.cellRows;
    if (rows.isEmpty) return [];
    for (int r = 0; r < rows.length; r++) {
      for (int c = 0; c < rows[r].length; c++) {
        final char = rows[r][c];
        if (_charToColor.containsKey(char)) {
          cells.add(
            PixelCell(row: r, col: c, targetColorId: _charToColor[char]!),
          );
        } else {
          cells.add(PixelCell(row: r, col: c, targetColorId: 0));
        }
      }
    }
    return cells;
  }

  static List<List<SilhouetteCellType>> generateSilhouetteGridFromMask(
    List<String> mask,
  ) {
    return List.generate(mask.length, (r) {
      final row = mask[r];
      return List.generate(row.length, (c) {
        final char = row[c];
        if (char == '.') {
          return SilhouetteCellType.outside;
        } else if (char == '#') {
          return SilhouetteCellType.outline;
        } else {
          return SilhouetteCellType.inside;
        }
      });
    });
  }


